package IA.Gasolina;

public class Camion {
    
    private int CoordX;
    private int CoordY;

    public Camion(int x, int y){
        this.CoordX = x;
        this.CoordY = y;
    }

    public int getCoordX() {
        return CoordX;
    }

    public int getCoordY() {
        return CoordY;
    }
}
